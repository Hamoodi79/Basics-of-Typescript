// Variable declaration and assignment with type annotations
let name: string = "Hamoodi";
let age: number = 25;
let isStudent: boolean = true;
let favoriteMeals: string[] = ["doroWot", "Pasta", "firfir"];

// Outputting the values
console.log("Name:", name);
